    return [UIColor colorWithRed:219/255.0f green:219/255.0f blue:219/255.0f alpha:1.0f];
}

+ (UIColor *)FDTDeepBlueColor {
    return [UIColor colorWithRed:47/255.0f green:81/255.0f blue:152/255.0f alpha:1.0f];
}

@end
